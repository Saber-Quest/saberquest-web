/**
 * Enum representing different item rarities.
 */
export enum ItemRarity {
  /**
   * Common item rarity.
   */
  Common = 1,

  /**
   * Uncommon item rarity.
   */
  Uncommon = 2,

  /**
   * Rare item rarity.
   */
  Rare = 3,

  /**
   * Epic item rarity.
   */
  Epic = 4,

  /**
   * Legendary item rarity.
   */
  Legendary = 5,
}
